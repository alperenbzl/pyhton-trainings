
# toplama
def Topla(degisken1, degisken2):
    return degisken1 + degisken2

# cikarma
def Cikar(degisken1,degisken2):
    return degisken2 - degisken1

# carpma
def Carpma(degisken1,degisken2):
    return degisken1 * degisken2

# bolme
def Bolme (degisken2,degisken1):
    return degisken2 / degisken1
